/**
 * Create colormap as array of THREE.Color objects
 * @param {string} name - Colormap name
 * @param {number} [numColors=256] - Number of discrete colors
 * @returns {Array<THREE.Color>} Array of colors
 */
export function createColormap(name, numColors = 256) {
  const cmapName = name.toLowerCase();
  
  // Check if colormap has explicit RGB data in COLORMAPS object
  const cmapData = COLORMAPS[cmapName];
  if (cmapData && Array.isArray(cmapData) && Array.isArray(cmapData[0])) {
    // Use explicit RGB array data (e.g., viridis, plasma)
    return cmapData.map(rgb => new THREE.Color(rgb[0], rgb[1], rgb[2]));
  }
  
  // If cmapData is a string, use it as the type for procedural generation
  const cmapType = (typeof cmapData === 'string') ? cmapData : cmapName;
  
  // For other colormaps, generate procedurally
  const colors = [];
  
  for (let i = 0; i < numColors; i++) {
    const t = i / (numColors - 1); // Normalized position [0, 1]
    colors.push(getColorForType(cmapType, t));
  }
  
  return colors;
}

/**
 * Get color for a specific colormap type at position t
 * @param {string} type - Colormap type
 * @param {number} t - Position in [0, 1]
 * @returns {THREE.Color}
 */
function getColorForType(type, t) {
  switch (type) {
    case 'rainbow':
      return getRainbowColor(t);
    
    case 'cooltowarm':
      return getCoolToWarmColor(t);
    
    case 'temperature':
    case 'hot':
      return getTemperatureColor(t);
    
    case 'bw':
    case 'grayscale':
      return new THREE.Color(t, t, t);
    
    default:
      // Default to rainbow
      return getRainbowColor(t);
  }
}

/**
 * Rainbow colormap (HSV-based)
 */
function getRainbowColor(t) {
  // Map t from [0,1] to hue [0.67 (blue), 0 (red)]
  const hue = (1 - t) * 0.67;
  return new THREE.Color().setHSL(hue, 1.0, 0.5);
}

/**
 * Cool to warm diverging colormap
 */
function getCoolToWarmColor(t) {
  const r = t;
  const g = 1 - Math.abs(2 * t - 1);
  const b = 1 - t;
  return new THREE.Color(r, g, b);
}

/**
 * Temperature colormap (black -> red -> yellow -> white)
 */
function getTemperatureColor(t) {
  let r, g, b;
  
  if (t < 0.33) {
    // Black to red
    r = t / 0.33;
    g = 0;
    b = 0;
  } else if (t < 0.67) {
    // Red to yellow
    r = 1;
    g = (t - 0.33) / 0.34;
    b = 0;
  } else {
    // Yellow to white
    r = 1;
    g = 1;
    b = (t - 0.67) / 0.33;
  }
  
  return new THREE.Color(r, g, b);
}

